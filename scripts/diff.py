import json
import hashlib
import requests
from datetime import datetime

TARGETS = [
    {"id": "consumer", "url": "https://www.ag.state.mn.us/consumer/"},
    {"id": "data_requests", "url": "https://www.ag.state.mn.us/Data-Requests/"},
    {"id": "scams", "url": "https://www.ag.state.mn.us/consumer/scams/"},
    {"id": "press", "url": "https://www.ag.state.mn.us/Office/PressRelease/"},
]

LEDGER_PATH = "ledger/ledger.jsonl"
DIFF_PATH = "site/diff.json"

def sha256(x: str) -> str:
    return hashlib.sha256(x.encode()).hexdigest()

def fetch(url):
    try:
        return requests.get(url, timeout=10).text
    except:
        return ""

def load_ledger():
    try:
        with open(LEDGER_PATH) as f:
            return [json.loads(l) for l in f if l.strip()]
    except:
        return []

def main():
    ledger = load_ledger()

    diff_rows = []

    # detect prior diff/baseline receipts
    has_any = any(
        r.get("type") in ["DIFF_RECEIPT", "BASELINE_RECEIPT"]
        for r in ledger
    )

    # 🚨 FORCE BASELINE IF EMPTY
    if not has_any:
        for t in TARGETS:
            body = fetch(t["url"])
            h = sha256(body)

            diff_rows.append({
                "receipt_id": f"BASELINE-{t['id']}",
                "source_url": t["url"],
                "prev_source_hash": None,
                "source_hash_sha256": h,
                "state": "seeded",
                "time": datetime.utcnow().isoformat() + "Z"
            })

    # (future diff logic can extend here — but baseline is guaranteed)

    with open(DIFF_PATH, "w") as f:
        json.dump({
            "description": "diff.json is generated by Actions from ledger.jsonl",
            "schema": "array of {receipt_id, source_url, prev_source_hash, source_hash_sha256, state}",
            "data": diff_rows
        }, f, indent=2)

if __name__ == "__main__":
    main()
